import {existsSync, readFileSync, readdirSync, writeFileSync} from 'node:fs'
import {resolve} from 'node:path'

const ROOT = resolve(import.meta.dirname, '..')
const OUTPUT = resolve(ROOT, 'THIRD_PARTY_LICENSES.md')
const FONT_LICENSE = resolve(ROOT, 'src/app/fonts/LICENSE-PRETENDARD.txt')
const CHECK_ONLY = process.argv.includes('--check')

const rootPackage = JSON.parse(readFileSync(resolve(ROOT, 'package.json'), 'utf8'))

const resolveRepository = (pkg) => {
    const repository = typeof pkg.repository === 'string' ? pkg.repository : pkg.repository?.url
    return (repository ?? pkg.homepage ?? '').replace(/^git\+/, '').replace(/\.git$/, '')
}

const findLicenseFile = (packageDirectory) =>
    readdirSync(packageDirectory)
        .filter((file) => /^(licen[cs]e|copying|notice)(\.|$)/i.test(file))
        .sort()[0]

const sections = Object.keys(rootPackage.dependencies)
    .sort((a, b) => a.localeCompare(b))
    .map((name) => {
        const packageDirectory = resolve(ROOT, 'node_modules', ...name.split('/'))
        const manifestPath = resolve(packageDirectory, 'package.json')
        if (!existsSync(manifestPath)) throw new Error(`${name}: yarn install 후 라이선스 문서를 생성하세요.`)

        const pkg = JSON.parse(readFileSync(manifestPath, 'utf8'))
        const licenseFile = findLicenseFile(packageDirectory)
        if (!licenseFile) throw new Error(`${name}@${pkg.version}: LICENSE/NOTICE 파일을 찾을 수 없습니다.`)

        const repository = resolveRepository(pkg)
        const metadata = [
            `## ${pkg.name}@${pkg.version}`,
            '',
            `- License: ${pkg.license ?? 'See license text below'}`,
            repository ? `- Source: ${repository}` : null,
            `- Included license file: ${licenseFile}`,
            '',
            '```text',
            readFileSync(resolve(packageDirectory, licenseFile), 'utf8').trim(),
            '```',
        ]
        return metadata.filter((line) => line !== null).join('\n')
    })

const fontLicense = readFileSync(FONT_LICENSE, 'utf8').trim()
sections.push(
    [
        '## Pretendard Variable Font',
        '',
        '- License: SIL Open Font License 1.1',
        '- Source: https://github.com/orioncactus/pretendard',
        '- Bundled asset: src/app/fonts/PretendardVariable.woff2',
        '- Included license file: src/app/fonts/LICENSE-PRETENDARD.txt',
        '',
        '```text',
        fontLicense,
        '```',
    ].join('\n'),
)

const output = [
    '# Third-Party Licenses',
    '',
    '> Generated by `yarn license-notices`. Do not edit this file manually.',
    '',
    'This document contains notices for the direct runtime dependencies declared in `package.json` and the bundled Pretendard font. Keep this file with source handoffs and redistributed application packages.',
    '',
    'Transitive packages installed by Yarn retain their own LICENSE/NOTICE files under `node_modules`. If dependencies are vendored or redistributed separately, preserve those package-level notices as well.',
    '',
    ...sections.flatMap((section) => [section, '']),
].join('\n')

if (CHECK_ONLY) {
    const current = existsSync(OUTPUT) ? readFileSync(OUTPUT, 'utf8') : ''
    if (current !== output) {
        console.error('❌ THIRD_PARTY_LICENSES.md가 현재 dependencies와 다릅니다. yarn license-notices를 실행하세요.')
        process.exit(1)
    }
    console.log('✅ THIRD_PARTY_LICENSES.md 최신 상태 확인')
} else {
    writeFileSync(OUTPUT, output)
    console.log(`✅ THIRD_PARTY_LICENSES.md 생성 완료 (${sections.length}개 고지)`)
}
